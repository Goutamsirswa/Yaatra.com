<p>While everyone knows Jodhpur is nicknamed the Blue City, there is no definitive explanation for how or why the sobriquet came to be. However, here are some of the theories as to why Indiaâ€™s Jodhpur is known as the Blue City.
First things first, of course, not all Jodhpur is painted blue, only the old town area near the Mehrangarh Fort has buildings mainly painted in blue hues. Standing atop the Mehrangarh Fort and gazing at the blue-patterned houses is a delight for the eyes and a great photo opportunity.</p>  
<h2 class="img_sub_head">Blue defines the Social Status </h2>
            <p>
                Yet another belief is that the blue colour is determinant of social status. This story goes back centuries when the Hindu caste system was considered to be the basis for determining the social status. According to the local lore, the Brahmin community (the priestly caste of India) painted their houses blue in order to differentiate themselves from the lower-caste communities, and since that time, blue is linked with the Brahmins. You might well hear these buildings referred to as Brahmin houses.
            </p>
