         <p>There are those who believe that the blue colour help deter termites. According to this idea, termites damaged many historic structures and buildings in the city. So, in order to keep their property safe from these unwelcoming pests, the residents painted their houses with blue paint. This paint is a mixture of copper sulphate and limestone that not only wards off bugs, but also imparts a soothing and calming effect. The residents who advocate this theory strictly oppose the belief that blue is associated with the Brahmins. In their favour, they state that there are just as many families from other castes living in the blue-painted houses.</p>
         <h2 class="img_sub_head">To keep the houses cool</h2>
         <p>Some locals believe that the colour blue is a good reflector of sun rays, so painting the house like this will keep their house cool in warmer months.

            Speaking to locals will get you a host of new theories and reasons. Regardless of the truth, the blue landscape is aesthetically pleasing nonetheless.</p>
