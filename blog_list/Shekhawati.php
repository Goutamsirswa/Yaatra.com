    <h2 class="img_sub_head red">What is a Shekhawati Haveli? </h2>
    <div class="img-div">
    <img src="../img/blog/haveli.jpg" class="img">
    </div>
                <p>
                    Well, Shekhawati Haveli is a Haveli located in the Shekhawati region of Rajasthan. This is a rather greener part of Rajasthan, and in good old days served as gardens for Rao Shekha, getting the name Shekhawati literally meaning the gardens of the Shekha. Different cities in this region were established by his sons.

                    Havelis are like huge mansions, with open courtyards and clearly demarcated public and private areas of business families that primarily inhabited them. 
                </p>
    <h2 class="img_sub_head red">When were Shekhawati Havelis Built?</h2>
    <p>Though you will find some Havelis built in the 19th CE, most of them were built in the early 20th CE.</p>
    <h2 class="img_sub_head red">Who Built these Opulent Havelis?</h2>
    <p>Businessmen of the Vaishya or Agrawal community primarily built these Havelis. When the cities were set up, they needed financial muscles to keep them going. So, businessmen from the neighborhood were invited to the city. These business families came and settled on the outskirts of the city, where they were given lands. They started trading in opium and spices. Remember this region is not too far from the ancient silk route.
    </p>
    <div class="img-div">
    <img src="../img/blog/laxmimata.jpg" class="img">
    </div>
    <p>After a few years, most of these families moved from here to Calcutta, following the opportunity to create more wealth. More money was put into getting bigger Havelis made and painted. There was a competition to build the biggest and most opulent Havelis.

        If you ask around, you would realize that most of the business families in India, especially the ones with their establishments in Kolkata have roots here.</p>
        <h2 class="img_sub_head red">Where are these Havelis Built?</h2>
        <p>Painted Havelis of Shekhawati can be found in Nawalgarh, Mandawa, Churu, Jhunjhunu, Bissau, Mahansar, Ramgarh, Fatehpur.

            Almost in every town, you would find Havelis in a cluster just outside the city gates, wherever there are gates, or outside the main bazaar area. The reason being most of these families came together to the region and got the land close to each other.</p>
<h2 class="img_sub_head red">The architecture of a Shekhawati Haveli</h2>
<p>A haveli has many influences and many innovations in its architecture. Most of these are situated in narrow lanes, close to each other. They probably did not have to budget for car parking inside or outside the Haveli. The outer walls are painted with larger paintings usually depicting the times of the British. Carriages, Trains, English lifestyles are common themes.</p>
<div class="img-div">
<img src="../img/blog/entrydoor.jpg" class="img">
</div>
<p>In some Havelis, Trains have been painted even before the first train arrived in India. The Seths were trying to educate the people in their village about the new mode of transportation. In one Haveli in Mandawa, I also saw an airplane painted.

    Most Havelis have two floors, but I did see a few with a single floor and one with three floors. The roof is a great place to see the city. Almost every other Haveli and prominent building like temples or forts or wells can be seen from the rooftops.</p>

<h2 class="img_sub_head red">Baithak</h2>
<p>Baithak is a large pillared hall with a single or double story, an equivalent of a conference room. The floor has white mattresses, bolsters, and cushions thrown around. A handheld fan hangs over, that was operated by a deaf and dumb person so that nothing goes out of the room through him.  Around the main area of the Baithak are safe rooms, where the Tijori or the cash would be kept. You can still see the metal safes in these rooms.</p>
<div class="img-div">
<img src="../img/blog/Baithak.jpg" class="img">
</div>
<p>The top floor has small window openings for the women of the house to listen to business discussions. The head of the family would often discuss the details with his wife or other women in the family about business. Remember, all these businesses used to be family businesses.</p>
